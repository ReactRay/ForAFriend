import mongoose from 'mongoose'

const requestSchema = new mongoose.Schema(
  {
    post: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'Post',
      required: true,
      index: true,
    },
    sender: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
      index: true,
    },
    receiver: {
      type: mongoose.Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    status: {
      type: String,
      enum: ['pending', 'rejected', 'waiting for payment', 'working'],
      required: true,
    },
  },
  { timestamps: true }
)

const Request = mongoose.model('Request', requestSchema)

export default Request
